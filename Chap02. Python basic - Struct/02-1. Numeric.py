# 정수형
a = 123
a = -178
a = 0 #기본

# 실수형
a = 1.2
a = -3.45

a = 4.24E10 # 4.24 * 10의 10승
a = 4.24e-10 # 4.24 * 10의 -10승

# 8진수
a = 0o177

# 16진수
a = 0x8ff
b = 0xABC

# 사칙연산
a = 3
b = 4
print("a + b = %d" % (a + b))

print("a - b = %d" % (a - b))

print("a * b = %d" % (a * b))

print("a / b = %d" % (a / b))

# ** 연산자
print("a ** b = %d" % (a ** b)) #3의 4승이니 81이 나온다.

# // 연산자
print("a // b = %d" % (a // b)) #나머지를 버리고 나눗셈을 한다!

