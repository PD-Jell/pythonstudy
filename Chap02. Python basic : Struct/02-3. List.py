# 리스트의 수정, 변경과 삭제
# 리스트에서 하나의 값 수정하기
a = [1, 2, 3]
a[2] = 4
print(a) # [1, 2, 4]

# 리스트에서 연속된 범위의 값 수정하기
print(a[1:2]) # [2]
a[1:2] = ['a', 'b', 'c'] # index의 1과 2사이의 리스트를 바꾼다.
print(a) # [1, 'a', 'b', 'c', 4]

# []를 사용해 리스트 요소 삭제하기
a[1:3] = []
print(a) # [1, 'c', 4]
